				Local 'Artrepreneur' Speaks to Summer 2016 OCS Highlander Class
			</h1>

			<p>Though it's a common scene for high school students to have their eyes and attention fixated on some sort of gadgetry these days, the students participating in the “Omaha Code School: Highlander” summer code camp are fixated on Macbooks, and with good reason: to learn web development.</p>

			<p>Hans Bekale was invited to come speak to the Highlander students during their first week of class in this year's summer session. Bekale, a self-taught web developer and founder of the budding marketing upstart, Classless Agency LLC, shared his insight towards the creative process of web development, as well as the practical application of this skillset.</p> 

			<p>In addition to a rundown of his more recent projects, Hans gave the students at Highlander a sneak peek into his firm's most recent effort dubbed “The Connect”, a new web-based social application designed to connect users with “all things good” in the creative, entertainment, and community spheres, to name a few.</p>

			<p>Events such as concerts, art expos, church fundraising events, and even community volunteer projects can all be found through the new platform. Hans hopes this new undertaking will reduce the “awareness gap” amongst citizens in the metro, and encourage individuals to seek out experiences that will not only build community, but build a sense of purpose.</p>
